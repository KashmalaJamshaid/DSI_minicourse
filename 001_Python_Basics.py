# LISTS in python 
my_list = [ 5, 6, 1]    #Output: [5, 6, 1]
my_list.append(4)       # For passing another value in the list, #Output: [5, 6, 1, 4]
my_list[0]              # For accessing values in the list placed on zero index #Output: 5

# Aggregation in LISTS
sum(my_list)  #Output: 16
max (my_list) #Output: 6
min (my_list) #Output: 1


# For LOOP in python
for value in my_list:
    print(value * value) #Output: 23 36 1 16